# 7.Write a python program to implements function on the string
# - capitalize
# - center
# - count
# - find
# - lower
# - upper
# - replace
# - split
# - swap-case


testString=input("Enter String: ")

print(f"Capitalize: {testString.capitalize()}")
print(f"Center: {testString.center(50)}")
print(f"count: {testString.count('s')}")
print(f"find: {testString.find('s')}")
print(f"lower: {testString.lower()}")
print(f"upper: {testString.upper()}")
print(f"replace: {testString.replace('M','S')}")
print(f"split: {testString.split(' ')}")
print(f"swap-case: {testString.swapcase()}")
